# List
fruits = ["apple", "banana", "cherry"]
fruits.append("orange")
print(fruits)

# Dictionaries
person = {"name": "John", "age": 30}
print(person["name"])

# Tuples and sets
tuple_example = (1, 2, 3)
set_example = {1, 2, 3}
print(tuple_example)
print(set_example)


fruits = ["apple", "banana", "cherry"]
print(fruits)

# Access elements
print(fruits[0])  # First element
print(fruits[-1])  # Last element

# Add an element
fruits.append("orange")
print(fruits)

# Remove an element
fruits.remove("banana")
print(fruits)


# tuples
colors = ("red", "green", "blue")
print(colors)

# Access elements
print(colors[1])  # Second element

# Tuples are immutable
# colors[1] = "yellow"  # This would raise an error



# dictionaries
person = {"name": "Alice", "age": 25, "city": "New York"}
print(person)

# Access a value
print(person["name"])

# Add a new key-value pair
person["profession"] = "Engineer"
print(person)

# Remove a key-value pair
del person["age"]
print(person)


# sets
numbers = {1, 2, 3, 3, 4}
print(numbers)

# Add an element
numbers.add(5)
print(numbers)

# Remove an element
numbers.remove(2)
print(numbers)
