# Performing arithmetic operations
x = 10
y = 5

# Addition
add = x + y
print("Addition:", add)

# Subtraction
subtract = x - y
print("Subtraction:", subtract)

# Multiplication
multiply = x * y
print("Multiplication:", multiply)

# Division
divide = x / y
print("Division:", divide)

# Modulus (remainder)
modulus = x % y
print("Modulus:", modulus)

# Exponentiation (x raised to the power of y)
power = x ** y
print("Exponentiation:", power)

# Floor division (division rounded down)
floor_divide = x // y
print("Floor Division:", floor_divide)